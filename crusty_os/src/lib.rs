//! crusty_os kernel library crate.
//!
//! Exposes the kernel's hardware-facing subsystems (GDT, IDT, interrupt
//! controllers, heap allocator, VGA text buffer) and re-exports the shared
//! test infrastructure that integration tests depend on.
//!
//! # Architecture
//!
//! ```text
//! crusty_os  ──depends on──▶  framework  (output + test runner, no hardware)
//!    │        ──depends on──▶  platform   (UART, QEMU exit, x86_64 I/O)
//!    │        ──depends on──▶  barnacle   (Multiboot2 boot — default)
//!    │        ──depends on──▶  abalone    (allocator implementations)
//!    │
//!    └── integration tests use crusty_os::test_runner (wraps framework::runner
//!                              + calls platform::exit_success on completion)
//! ```
//!
//! # Test infrastructure
//!
//! Lib-level tests (`cargo test -p crusty_os --lib`) use the entry point
//! `test_kernel_main` below, generated by `barnacle::entry_point!`.
//! Integration tests (in `tests/`) set `#![test_runner(crusty_os::test_runner)]`
//! and call `unsafe { platform::init() }` at their own entry point.
//!
//! [`test_panic_handler`] is provided for integration tests that use
//! `harness = false` so they can still report failures via serial.

#![no_std]
#![cfg_attr(test, no_main)]
#![feature(custom_test_frameworks)]
#![test_runner(framework::runner)]
#![reexport_test_harness_main = "test_main"]
#![feature(abi_x86_interrupt)]

use core::panic::PanicInfo;

pub mod serial;
pub mod vga_buffer;
pub mod interrupts;
pub mod gdt;
pub mod allocator;

